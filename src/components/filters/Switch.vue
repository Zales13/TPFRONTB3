<template>
  <label class="switch" :for="id" @click="test">
    <input :id="id" type="checkbox" v-model="value"/>
    <span class="slider round"></span>
  </label>
</template>

<script>
  export default {
    name: 'Switch',

    props: ['modelValue', 'id'],
    emits: ['update:modelValue'],

    computed: {
      value: {
        get() {
          return this.modelValue;
        },
        set(value) {
          this.$emit('update:modelValue', value);
        },
      },
    },
  };
</script>

